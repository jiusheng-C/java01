package com.jiusheng.finaldemo;

public class FinalDemo2 {
    public static void main(String[] args) {
        // 目标：详解常量。
        System.out.println(Constant.SYSTEM_NAME);
        System.out.println(Constant.SYSTEM_NAME);
        System.out.println(Constant.SYSTEM_NAME);
        System.out.println(Constant.SYSTEM_NAME);
        System.out.println(Constant.SYSTEM_NAME);
    }

    // 其实常量就是把某个变量给赋值后不能修改这个变量的值：变量->自变量。
    // 使用static final 修饰的成员变量就被称为常量。
}
